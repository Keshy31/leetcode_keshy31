class Solution:
    def average(self, salary: list[int]) -> float:
        print(f"Salary = {salary}")
        minimum = min(salary)
        print(f"Min = {minimum}")
        maximum = max(salary)
        print(f"Max = {maximum}")
        length = len(salary)
        print(f"N = {length}")

        return 1.0


salary = [4000, 3000, 1000, 2000]
